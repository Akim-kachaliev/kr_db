-- 1. Таблица Роль (Role)
CREATE TABLE Роль (
   Номер_роли SERIAL PRIMARY KEY,
   Наименование VARCHAR(50) NOT NULL,
   
   CONSTRAINT uq_role_name UNIQUE (Наименование)
);

-- 2. Таблица Должность (Position)
CREATE TABLE Должность (
   Номер_должности SERIAL PRIMARY KEY,
   Наименование VARCHAR(100) NOT NULL,
   Базовый_оклад DECIMAL(10, 2) NOT NULL,
   
   CONSTRAINT uq_position_name UNIQUE (Наименование),
   CONSTRAINT chk_salary CHECK (Базовый_оклад > 0)
);

-- 3. Таблица Квалификация (Qualification)
CREATE TABLE Квалификация (
   Номер_квалификации SERIAL PRIMARY KEY,
   Наименование VARCHAR(100) NOT NULL,
   Коэффициент DECIMAL(3, 2) NOT NULL,
   
   CONSTRAINT chk_coeff CHECK (Коэффициент > 0 AND Коэффициент <= 5.00)
);

-- 4. Таблица Вид_работы (Work_Type)
CREATE TABLE Вид_работы (
   Код_вида_работы SERIAL PRIMARY KEY,
   Наименование VARCHAR(150) NOT NULL,
   Описание TEXT,
   Базовая_оплата DECIMAL(10, 2) NOT NULL,
   Нормативная_длительность DECIMAL(5, 2) NOT NULL,
   
   CONSTRAINT chk_payment CHECK (Базовая_оплата > 0),
   CONSTRAINT chk_duration CHECK (Нормативная_длительность > 0)
);

-- 5. Таблица List (Пользователь)
CREATE TABLE List (
   Номер_пользователя SERIAL PRIMARY KEY,
   Номер_роли INT NOT NULL,
   Имя VARCHAR(100) NOT NULL,
   Логин VARCHAR(50) NOT NULL,
   email VARCHAR(100) NOT NULL,
   Дата_регистрации DATE NOT NULL DEFAULT CURRENT_DATE,
   Хеш_пароля VARCHAR(255) NOT NULL,
   
   CONSTRAINT uq_login UNIQUE (Логин),
   CONSTRAINT uq_email UNIQUE (email),
   FOREIGN KEY (Номер_роли) REFERENCES Роль(Номер_роли) ON DELETE RESTRICT
);

-- 6. Таблица Сотрудник (Employee)
CREATE TABLE Сотрудник (
   Номер_сотрудника SERIAL PRIMARY KEY,
   Фамилия VARCHAR(50) NOT NULL,
   Имя VARCHAR(50) NOT NULL,
   Отчество VARCHAR(50),
   Телефон VARCHAR(20),
   Дата_рождения DATE,
   Дата_приема DATE NOT NULL DEFAULT CURRENT_DATE,
   Номер_квалификации INT NOT NULL,
   Номер_должности INT NOT NULL,
   Номер_пользователя INT,
   
   CONSTRAINT uq_employee_user UNIQUE (Номер_пользователя),
   FOREIGN KEY (Номер_квалификации) REFERENCES Квалификация(Номер_квалификации) ON DELETE RESTRICT,
   FOREIGN KEY (Номер_должности) REFERENCES Должность(Номер_должности) ON DELETE RESTRICT,
   FOREIGN KEY (Номер_пользователя) REFERENCES List(Номер_пользователя) ON DELETE SET NULL
);

-- 7. Таблица Дополнительная_работа (Additional_Work)
CREATE TABLE Дополнительная_работа (
   Номер_доп_работы SERIAL PRIMARY KEY,
   Наименование VARCHAR(200) NOT NULL,
   Описание TEXT,
   Дата_создания DATE NOT NULL DEFAULT CURRENT_DATE,
   Плановое_начало DATE NOT NULL,
   Плановое_окончание DATE NOT NULL,
   Плановое_число_исполнителей INT NOT NULL DEFAULT 1,
   Код_вида_работы INT NOT NULL,
   
   CONSTRAINT chk_dates CHECK (Плановое_окончание >= Плановое_начало),
   CONSTRAINT chk_workers CHECK (Плановое_число_исполнителей >= 1),
   FOREIGN KEY (Код_вида_работы) REFERENCES Вид_работы(Код_вида_работы) ON DELETE RESTRICT
);

-- 8. Таблица Назначение (Assignment)
CREATE TABLE Назначение (
   Номер_назначения SERIAL PRIMARY KEY,
   Номер_сотрудника INT NOT NULL,
   Номер_доп_работы INT NOT NULL,
   Дата_назначения DATE NOT NULL DEFAULT CURRENT_DATE,
   Начало_работ DATE,
   Дата_завершения DATE,
   Сумма_доплаты DECIMAL(10, 2),
   
   CONSTRAINT uq_employee_work UNIQUE (Номер_сотрудника, Номер_доп_работы),
   CONSTRAINT chk_assign_dates CHECK (Дата_завершения IS NULL OR Начало_работ IS NULL OR Дата_завершения >= Начало_работ),
   FOREIGN KEY (Номер_сотрудника) REFERENCES Сотрудник(Номер_сотрудника) ON DELETE CASCADE,
   FOREIGN KEY (Номер_доп_работы) REFERENCES Дополнительная_работа(Номер_доп_работы) ON DELETE CASCADE
);

